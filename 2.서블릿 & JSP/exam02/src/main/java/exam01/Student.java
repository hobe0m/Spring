package exam01;

public class Student {
    private int id; // 학번이므로 Int(정수) 타입
    private String name; // 학생명이므로 String 타입
    private String subject; // 과목이므로 String 타입

    public Student(int id, String name, String subject) {
        this.id = id;
        this.name = name;
        this.subject = subject;
    }
}


